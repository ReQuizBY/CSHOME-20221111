// По заданному с клавиатуры номеру дня недели вывести его название

System.Console.WriteLine("Введите день недели:");
string d = Console.ReadLine()!;

if (d == "1") d = "Понедельник"; 
if (d == "2") d = "Вторник"; 
if (d == "3") d = "Среда"; 
if (d == "4") d = "Четверг"; 
if (d == "5") d = "Пятница"; 
if (d == "6") d = "Суббота"; 
if (d == "7") d = "Воскресенье"; 
System.Console.WriteLine($" Название дня недели = {d}");
